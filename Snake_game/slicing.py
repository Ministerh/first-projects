keys_list = ["a","b","c", "d", "e", "f", "g", "h"]
#using slicing to print then the following can be printed
print(keys_list[3:])
#this line will print from "d" the till the last element of the list
print(keys_list[:4])
#this line will print from the first element to "d"
print(keys_list[3::2])
#this line will print from the "d" to the last element skipping an element
print(keys_list[::-1])
#this line will print every element in the list in reverse order

